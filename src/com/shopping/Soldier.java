package com.shopping;

public class Soldier
{
    private int strength;
    private int cost;

    void setStrength(int s1)
    {
        strength = s1;
    }

    void setCost(int c1)
    {
        cost = c1;
    }

    int getStrength()
    {
        return strength;
    }

    int getCost()
    {
        return cost;
    }
}
